<p>C Mand 中文编程是脱胎于C++的一种中文的编程语言，<strong>Mandarin</strong> 原意即 <code v-pre>普通话</code>，也表明其想要成为中文编程领域的一种<code v-pre>普通话</code>的编程语言。（在本文档以后称其为<code v-pre>CMand</code>值得默认为该编程语言）</p>
