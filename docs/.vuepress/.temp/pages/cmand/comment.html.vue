<template><div><h2 id="注释" tabindex="-1"><a class="header-anchor" href="#注释"><span>注释</span></a></h2>
<p>在CMand中，注释跟C++是一样的，但是如果你不了解C++也不用慌，因为注释非常简单</p>
<h3 id="_1-单行注释" tabindex="-1"><a class="header-anchor" href="#_1-单行注释"><span>1. 单行注释</span></a></h3>
<p>单行注释以<code v-pre>//</code>开头，注释内容直到行尾结束。</p>
<div class="language-cpp line-numbers-mode" data-highlighter="prismjs" data-ext="cpp"><pre v-pre><code><span class="line"><span class="token comment">// 这是一个单行注释</span></span>
<span class="line"></span></code></pre>
<div class="line-numbers" aria-hidden="true" style="counter-reset:line-number 0"><div class="line-number"></div></div></div><h3 id="_2-多行注释" tabindex="-1"><a class="header-anchor" href="#_2-多行注释"><span>2. 多行注释</span></a></h3>
<p>多行注释以<code v-pre>/*</code>开头，以<code v-pre>*/</code>结尾，注释内容可以跨越多行。</p>
<div class="language-cpp line-numbers-mode" data-highlighter="prismjs" data-ext="cpp"><pre v-pre><code><span class="line"><span class="token comment">/*</span>
<span class="line">这是一个多行注释</span>
<span class="line">*/</span></span>
<span class="line"></span></code></pre>
<div class="line-numbers" aria-hidden="true" style="counter-reset:line-number 0"><div class="line-number"></div><div class="line-number"></div><div class="line-number"></div></div></div><h3 id="_3-易语言注释" tabindex="-1"><a class="header-anchor" href="#_3-易语言注释"><span>3. 易语言注释</span></a></h3>
<p>如果你使用的是简易IDE的话，那么可以直接使用易语言的注释方式。</p>
<div class="language-text line-numbers-mode" data-highlighter="prismjs" data-ext="text"><pre v-pre><code><span class="line">‘ 这是一个注释</span>
<span class="line"></span></code></pre>
<div class="line-numbers" aria-hidden="true" style="counter-reset:line-number 0"><div class="line-number"></div></div></div><blockquote>
<p>单引号后面就是注释。</p>
</blockquote>
</div></template>


